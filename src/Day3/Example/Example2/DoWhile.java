package Day3.Example.Example2;

public class DoWhile {
  public static void main(String[] args) {
    // In ra bang cuu chuong
    
    for(int i = 1; i <= 9; i++) {
      for(int j = 1; j <= 10; j++) {
        System.out.printf("\n" + i + " * " + j + " = " + (i * j));
      }
      System.out.println("\n---------------");
    }

  }
}
